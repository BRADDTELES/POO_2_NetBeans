    private void listarTodosButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listarTodosButtonActionPerformed
        // TODO add your handling code here:
        Principal.contatos = Principal.contatoDao.getListaContatos();

        if (Principal.contatos.isEmpty() == false) {
            TodosForm todosForm = new TodosForm();
            todosForm.setVisible(true);
            todosForm.setLocationRelativeTo(null);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Nenhum contato encontrado!");
        }


    }//GEN-LAST:event_listarTodosButtonActionPerformed

