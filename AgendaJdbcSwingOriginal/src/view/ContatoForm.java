    private void cadastrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarButtonActionPerformed

        Principal.contato.setNome(nomeTextField.getText());
        Principal.contato.setFone(foneTextField.getText());
        Principal.contato.setEmail(emailTextField.getText());

        boolean inseriu = Principal.contatoDao.inserir(Principal.contato);

        if (inseriu) {
            JOptionPane.showMessageDialog(this, "Contato cadastrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Falha ao cadastrar contato!");
        }

    }//GEN-LAST:event_cadastrarButtonActionPerformed

    private void atualizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarButtonActionPerformed

        Principal.contato.setId(Integer.parseInt(idTextField.getText()));
        Principal.contato.setNome(nomeTextField.getText());
        Principal.contato.setFone(foneTextField.getText());
        Principal.contato.setEmail(emailTextField.getText());

        boolean atualizou = Principal.contatoDao.atualizar(Principal.contato);

        if (atualizou) {
            JOptionPane.showMessageDialog(this, "Contato atualizado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Falha ao atualizar contato!");
        }

    }//GEN-LAST:event_atualizarButtonActionPerformed

    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed

        Principal.contato.setId(Integer.parseInt(idTextField.getText()));

        Principal.contato = Principal.contatoDao.buscar(Principal.contato.getId());

        if (Principal.contato == null) {
            JOptionPane.showMessageDialog(this, "Nenhum contato encontrado!");
            return;
        }

        if (Principal.contato.getId() == 0) {
            JOptionPane.showMessageDialog(this, "Nenhum contato encontrado!");
            return;
        }

        nomeTextField.setText(Principal.contato.getNome());
        emailTextField.setText(Principal.contato.getEmail());
        foneTextField.setText(Principal.contato.getFone());

    }//GEN-LAST:event_buscarButtonActionPerformed

